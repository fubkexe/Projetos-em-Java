
public class Guerreiro {
	public Guerreiro(String nome, int forca, int velocidade) {
		super();
		this.nome = nome;
		this.forca = forca;
		this.velocidade = velocidade;
	}
	String nome;
	int forca;
	int velocidade;

	public String getnome() {
		return nome;
	
	}
	public int getforca() {
		return forca;
	}
	public int getvelocidade() {
		return velocidade;
	}
}
